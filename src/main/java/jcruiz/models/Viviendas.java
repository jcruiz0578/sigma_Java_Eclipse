package jcruiz.models;

public class Viviendas {
	
	private String id_ingreso;
	private String tipovia;
	private String direccionest;
	private String zonaubicacion;
	private String tipovivienda;
	private String ubicacionvivienda;
	private String condicionvivienda;
	private String infraestructura;
	
	
	
	public String getId_ingreso() {
		return id_ingreso;
	}
	public void setId_ingreso(String id_ingreso) {
		this.id_ingreso = id_ingreso;
	}
	public String getTipovia() {
		return tipovia;
	}
	public void setTipovia(String tipovia) {
		this.tipovia = tipovia;
	}
	public String getDireccionest() {
		return direccionest;
	}
	public void setDireccionest(String direccionest) {
		this.direccionest = direccionest;
	}
	public String getZonaubicacion() {
		return zonaubicacion;
	}
	public void setZonaubicacion(String zonaubicacion) {
		this.zonaubicacion = zonaubicacion;
	}
	public String getTipovivienda() {
		return tipovivienda;
	}
	public void setTipovivienda(String tipovivienda) {
		this.tipovivienda = tipovivienda;
	}
	public String getUbicacionvivienda() {
		return ubicacionvivienda;
	}
	public void setUbicacionvivienda(String ubicacionvivienda) {
		this.ubicacionvivienda = ubicacionvivienda;
	}
	public String getCondicionvivienda() {
		return condicionvivienda;
	}
	public void setCondicionvivienda(String condicionvivienda) {
		this.condicionvivienda = condicionvivienda;
	}
	public String getInfraestructura() {
		return infraestructura;
	}
	public void setInfraestructura(String infraestructura) {
		this.infraestructura = infraestructura;
	}
	
	
	
	

}
